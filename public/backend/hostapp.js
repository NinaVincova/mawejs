// Access to Electron host system dialogs
//
//*****************************************************************************
//*****************************************************************************

module.exports = {
  quit,
  log,
  beep,
}

//-----------------------------------------------------------------------------

const {app, shell} = require("electron");

//-----------------------------------------------------------------------------

function quit() {
  app.quit();
}

function log(message) {
  console.log(message)
}

function beep() {
  console.log("Beep")
  shell.beep()
}
